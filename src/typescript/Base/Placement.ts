/**
 * Enumeration of compass-point placement positions plus center,
 * used to describe the relative position of a child within a container.
 */
export enum Placement {
    CENTER = "center",
    NORTH = "north",
    SOUTH = "south",
    WEST = "west",
    EAST = "east"
}
