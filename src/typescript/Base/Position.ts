/**
 * Enumeration of the standard CSS `position` property values.
 */
export enum Position {
    INITIAL = "initial",
    INHERIT = "inherit",
    STATIC = "static",
    RELATIVE = "relative",
    FIXED = "fixed",
    ABSOLUTE = "absolute",
    STICKY = "sticky"
}
